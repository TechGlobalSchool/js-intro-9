// Arrays

const arr = [1,2,3];

// push() - O(1)
arr.push(4) 

// pop() - O(1)
arr.pop()

// shift() - O(n) 
arr.shift() 
// [1,2,3] => [2,3]
//  0 1 2  =>  0 1

// unshift() - O(n)
arr.unshift(8) 
// [1,2,3] => [8,1,2,3]
//  0 1 2  =>  0 1 2 3

